const AV = require('../utils/av-weapp-min');

class Activity extends AV.Object {
  get done() {
    return this.get('done');
  }
  set done(value) {
    this.set('done', value);
  }

  get content() {
    return this.get('content');
  }
  set content(value) {
    this.set('content', value);
  }
}

AV.Object.register(Activity, 'Activity');
module.exports = Activity;